import * as THREE from 'three';
import { shaderMaterial } from '@react-three/drei';

export const ParticleMaterial = shaderMaterial(
  {
    uTime: 0,
    uColor: new THREE.Color(0.0, 1.0, 0.0),
    uProgress: 0, // 0 = Scatter, 1 = Tree
    uNoiseStrength: 0.5,
    uPixelRatio: 1,
    uSize: 10,
    uMouse: new THREE.Vector3(0, 0, 0),
    uInteract: 0, // 0 = No interaction, 1 = Full interaction
  },
  // Vertex Shader
  `
    uniform float uTime;
    uniform float uProgress;
    uniform float uNoiseStrength;
    uniform float uPixelRatio;
    uniform float uSize;
    uniform vec3 uMouse;
    uniform float uInteract;

    attribute vec3 treePosition;
    attribute float randomness;

    varying float vAlpha;
    varying float vDistance;

    // Simplex noise helper
    vec3 mod289(vec3 x) { return x - floor(x * (1.0 / 289.0)) * 289.0; }
    vec4 mod289(vec4 x) { return x - floor(x * (1.0 / 289.0)) * 289.0; }
    vec4 permute(vec4 x) { return mod289(((x*34.0)+1.0)*x); }
    vec4 taylorInvSqrt(vec4 r) { return 1.79284291400159 - 0.85373472095314 * r; }
    float snoise(vec3 v) {
      const vec2 C = vec2(1.0/6.0, 1.0/3.0);
      const vec4 D = vec4(0.0, 0.5, 1.0, 2.0);
      vec3 i  = floor(v + dot(v, C.yyy) );
      vec3 x0 = v - i + dot(i, C.xxx) ;
      vec3 g = step(x0.yzx, x0.xyz);
      vec3 l = 1.0 - g;
      vec3 i1 = min( g.xyz, l.zxy );
      vec3 i2 = max( g.xyz, l.zxy );
      vec3 x1 = x0 - i1 + C.xxx;
      vec3 x2 = x0 - i2 + C.yyy;
      vec3 x3 = x0 - D.yyy;
      i = mod289(i);
      vec4 p = permute( permute( permute(
                i.z + vec4(0.0, i1.z, i2.z, 1.0 ))
              + i.y + vec4(0.0, i1.y, i2.y, 1.0 ))
              + i.x + vec4(0.0, i1.x, i2.x, 1.0 ));
      float n_ = 0.142857142857;
      vec3  ns = n_ * D.wyz - D.xzx;
      vec4 j = p - 49.0 * floor(p * ns.z * ns.z);
      vec4 x_ = floor(j * ns.z);
      vec4 y_ = floor(j - 7.0 * x_ );
      vec4 x = x_ *ns.x + ns.yyyy;
      vec4 y = y_ *ns.x + ns.yyyy;
      vec4 h = 1.0 - abs(x) - abs(y);
      vec4 b0 = vec4( x.xy, y.xy );
      vec4 b1 = vec4( x.zw, y.zw );
      vec4 s0 = floor(b0)*2.0 + 1.0;
      vec4 s1 = floor(b1)*2.0 + 1.0;
      vec4 sh = -step(h, vec4(0.0));
      vec4 a0 = b0.xzyw + s0.xzyw*sh.xxyy ;
      vec4 a1 = b1.xzyw + s1.xzyw*sh.zzww ;
      vec3 p0 = vec3(a0.xy,h.x);
      vec3 p1 = vec3(a0.zw,h.y);
      vec3 p2 = vec3(a1.xy,h.z);
      vec3 p3 = vec3(a1.zw,h.w);
      vec4 norm = taylorInvSqrt(vec4(dot(p0,p0), dot(p1,p1), dot(p2, p2), dot(p3,p3)));
      p0 *= norm.x;
      p1 *= norm.y;
      p2 *= norm.z;
      p3 *= norm.w;
      vec4 m = max(0.6 - vec4(dot(x0,x0), dot(x1,x1), dot(x2,x2), dot(x3,x3)), 0.0);
      m = m * m;
      return 42.0 * dot( m*m, vec4( dot(p0,x0), dot(p1,x1), dot(p2,x2), dot(p3,x3) ) );
    }

    void main() {
      // 1. Calculate positions
      vec3 posScatter = position; // The initial buffer position (sphere)
      vec3 posTree = treePosition; // The target buffer position (cone)
      
      // 2. Mix based on progress
      // Use cubic ease in/out for smoother transitions
      float t = uProgress;
      float smoothT = t * t * (3.0 - 2.0 * t);
      vec3 currentPos = mix(posScatter, posTree, smoothT);

      // 3. Add Noise (Breathing / Wind)
      float noiseFreq = 0.5;
      float noiseAmp = uNoiseStrength * (1.0 + uInteract * 2.0); // Increase noise on interaction
      float timeOffset = uTime * 0.5 + randomness * 10.0;
      
      vec3 noiseVec = vec3(
        snoise(vec3(currentPos.x * noiseFreq, currentPos.y * noiseFreq, timeOffset)),
        snoise(vec3(currentPos.x * noiseFreq, currentPos.y * noiseFreq + 100.0, timeOffset)),
        snoise(vec3(currentPos.x * noiseFreq, currentPos.y * noiseFreq + 200.0, timeOffset))
      );
      
      currentPos += noiseVec * noiseAmp;

      // 4. Mouse Interaction (Repel or Attract) - mainly for Red particles
      if (uInteract > 0.0) {
        float d = distance(currentPos, uMouse);
        if (d < 3.0) {
          vec3 dir = normalize(currentPos - uMouse);
          // Push away slightly
          currentPos += dir * (3.0 - d) * 0.2 * uInteract; 
        }
      }

      vec4 mvPosition = modelViewMatrix * vec4(currentPos, 1.0);
      gl_Position = projectionMatrix * mvPosition;

      // Size attenuation
      gl_PointSize = uSize * uPixelRatio * (1.0 / -mvPosition.z);
      
      // Varing for fragment
      vAlpha = 1.0;
      vDistance = -mvPosition.z;
    }
  `,
  // Fragment Shader
  `
    uniform vec3 uColor;
    varying float vAlpha;

    void main() {
      // Soft circle sprite
      vec2 center = gl_PointCoord - 0.5;
      float dist = length(center);
      
      // Halo effect: bright center, soft edge
      float strength = 0.05 / dist - 0.1;
      strength = clamp(strength, 0.0, 1.0);
      
      // Add a hard core for "solid" feeling
      float core = 1.0 - smoothstep(0.0, 0.1, dist);
      
      vec3 finalColor = uColor * (strength + core * 0.5);

      if (strength < 0.01) discard;

      gl_FragColor = vec4(finalColor, strength * vAlpha);
      
      // Premultiplied alpha handling for additive blending if needed, 
      // but standard blending works well here.
      #include <tonemapping_fragment>
      #include <colorspace_fragment>
    }
  `
);